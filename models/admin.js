const mongoose = require("mongoose");

const adminsSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      default: false,
    },
  },
  {
    timestamps: true,
  }
);

const Admins = mongoose.model("Admin", adminsSchema);

module.exports = {
  Admins,
};
